package Week2Questions;

public class intAndInt {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int c = a + b;

        System.out.println("C = " + c);

        Integer x = 5;
        Integer y = 6;
        Integer z = x + y;

        System.out.println("Z = " + z);
    }
}
